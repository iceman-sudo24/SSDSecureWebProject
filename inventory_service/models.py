# =============================================================================

class InventoryAuditLog(models.Model):
    """
    Item-level audit trail recording who changed what and when.

    SET_NULL on foreign keys ensures audit records survive user/item deletion.
    The username snapshot captures the name at the time of the event.

    Maps to:
    - OWASP A09:2021 - Security Logging and Monitoring Failures
    - ASVS V1.2 - Architecture (audit trail)
    """

    class Action(models.TextChoices):
        CREATED = "CREATED", "Created"
        UPDATED = "UPDATED", "Updated"
        DELETED = "DELETED", "Deleted"
        VIEWED = "VIEWED", "Viewed"

    id = models.UUIDField(
        primary_key=True,
        default=uuid.uuid4,
        editable=False,
    )

    # SET_NULL so audit records survive item deletion
    item = models.ForeignKey(
        InventoryItem,
        on_delete=models.SET_NULL,
        null=True,
        blank=True,
        related_name="audit_logs",
        help_text="Item that was acted upon.",
    )
    item_name_snapshot = models.CharField(
        max_length=100,
        blank=True,
        help_text="Item name at the time of the event (preserved if item is deleted).",
    )

    # SET_NULL so audit records survive user deletion
    user = models.ForeignKey(
        settings.AUTH_USER_MODEL,
        on_delete=models.SET_NULL,
        null=True,
        blank=True,
        related_name="inventory_audit_logs",
        help_text="User who performed the action.",
    )
    username_snapshot = models.CharField(
        max_length=150,
        blank=True,
        help_text="Username at the time of the event.",
    )

    action = models.CharField(
        max_length=20,
        choices=Action.choices,
        help_text="Type of action performed.",
    )

    field_changed = models.CharField(
        max_length=100,
        blank=True,
        help_text="Name of the field that was changed (for UPDATED events).",
    )
    old_value = models.TextField(
        blank=True,
        help_text="Previous value before the change.",
    )
    new_value = models.TextField(
        blank=True,
        help_text="New value after the change.",
    )

    ip_address = models.GenericIPAddressField(
        null=True,
        blank=True,
        help_text="Client IP address at the time of the event.",
    )
    timestamp = models.DateTimeField(auto_now_add=True)

    class Meta:
        db_table = "inventory_audit_log"
        ordering = ["-timestamp"]
        verbose_name = "Inventory Audit Log"
        verbose_name_plural = "Inventory Audit Logs"

    def __str__(self):
        return (
            f"[{self.timestamp:%Y-%m-%d %H:%M}] "
            f"{self.action} on '{self.item_name_snapshot}' "
            f"by {self.username_snapshot}"
        )

    def save(self, *args, **kwargs):
        # Capture snapshots on first save so records survive FK deletion
        if not self.username_snapshot and self.user:
            self.username_snapshot = self.user.username
        if not self.item_name_snapshot and self.item:
            self.item_name_snapshot = self.item.name
        super().save(*args, **kwargs)